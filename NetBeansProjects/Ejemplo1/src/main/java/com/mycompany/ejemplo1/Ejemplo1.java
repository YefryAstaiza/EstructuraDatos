package com.mycompany.ejemplo1;
public class Ejemplo1 {

    public static void main(String[] args) {
        int a = 10;
        int b = 10;
        int suma = a + b;
        int resta = a - b;
        int divicion = a / b;
        int multi = a * b;
        System.out.println("la suma es:" + suma);
        System.out.println("la resta es:" + resta);
        System.out.println("la divicion es:" + divicion);
        System.out.println("la multiplicacion es:" + multi);
        System.out.println("la suma de " + a + " mas " + b +" es igual a: "+ suma);
        System.out.println("la resta de " + a + " menos " + b +" es igual a: "+ resta);
        System.out.println("la divicion de " + a + " dividido " + b +" es igual a: "+ divicion);
        System.out.println("la multiplicacion de " + a + " por " + b +" es igual a: "+ multi);
    }
}
